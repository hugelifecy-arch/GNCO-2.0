'use client'

import { useParams } from 'next/navigation'
import { useEffect, useState } from 'react'

export default function SharedResultsPage() {
  const params = useParams<{ shareId: string }>()
  const [results, setResults] = useState<Record<string, unknown> | null>(null)
  const [loading, setLoading] = useState(true)
  const [error, setError] = useState('')

  useEffect(() => {
    async function loadResults() {
      try {
        const response = await fetch(`/api/architect/share?id=${params.shareId}`)
        const data = (await response.json()) as {
          success?: boolean
          data?: Record<string, unknown>
        }

        if (data.success && data.data) {
          setResults(data.data)
        } else {
          setError('Results not found or expired')
        }
      } catch {
        setError('Failed to load results')
      } finally {
        setLoading(false)
      }
    }

    if (params.shareId) {
      loadResults()
    }
  }, [params.shareId])

  if (loading) {
    return (
      <div className="flex min-h-screen items-center justify-center bg-bg-primary">
        <div className="text-text-secondary">Loading results...</div>
      </div>
    )
  }

  if (error) {
    return (
      <div className="flex min-h-screen items-center justify-center bg-bg-primary">
        <div className="text-center">
          <h1 className="mb-2 text-2xl font-serif text-text-primary">Results Not Found</h1>
          <p className="mb-6 text-text-secondary">{error}</p>
          <a href="/" className="inline-block rounded-sm bg-accent-gold px-6 py-3 text-bg-primary">
            Go to GNCO Home
          </a>
        </div>
      </div>
    )
  }

  return (
    <div className="min-h-screen bg-bg-primary px-6 py-12">
      <div className="mx-auto max-w-5xl">
        <div className="mb-8 text-center">
          <div className="mb-4 flex items-center justify-center gap-2">
            <span className="font-serif text-xl text-accent-gold">◆ GNCO</span>
            <span className="text-text-tertiary">•</span>
            <span className="text-sm text-text-secondary">Shared Results</span>
          </div>
          <h1 className="mb-2 font-serif text-3xl text-text-primary">Fund Structure Analysis</h1>
          <p className="text-sm text-text-secondary">Generated by GNCO Architect Engine • Read-only view</p>
        </div>

        <div className="rounded-lg border border-bg-border bg-bg-surface p-8">
          <pre className="overflow-auto text-xs text-text-secondary">{JSON.stringify(results, null, 2)}</pre>
        </div>

        <div className="mt-12 text-center">
          <p className="mb-4 text-text-secondary">Want to run your own analysis?</p>
          <a
            href="/architect"
            className="inline-block rounded-sm bg-accent-gold px-8 py-4 font-semibold text-bg-primary transition-all hover:bg-accent-gold-light"
          >
            Try GNCO Architect Engine
          </a>
        </div>
      </div>
    </div>
  )
}
